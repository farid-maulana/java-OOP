package jti.polinema.relasikelas;

public class Rapor {
    private String mapel;
    private int nilai;

    public Rapor() {
    }

    public String getMapel() {
        return mapel;
    }

    public void setMapel(String mapel) {
        this.mapel = mapel;
    }

    public int getNilai() {
        return nilai;
    }

    public void setNilai(int nilai) {
        this.nilai = nilai;
    }
}
